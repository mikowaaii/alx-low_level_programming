#include "main.h"

/**
 * main - prints the program name, followed by a new line
 * @argc: the count of arguments
 * @argv: array of pointers to the string which are the arguments
 *
 * Return: Always 0
 */

int main(__attribute__((unused)) int argc, char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
